class Pokemon {
	constructor(type1 =0, type2 =0){
		this.type1 = type1;
		this.type2 = type2;
	}

    type(slot=1, newType){
        if(slot==1){
            type1 = newType;
        } 
        else {
            type2 = newType;
        }
    }

    get typeInSlot(slot){
        if(slot == 1)
            return type1;
        else
            return type2;
    }

    get typeName (slot) {
        return typeNames[typeInSlot(slot)];
    }
}

